package model;

public class ChiTietLoiModel {
	private String ID;
	private String Ngay_Bao_Loi;
	private String Thong_Tin_Loi;
	private String Ten_San_Pham;
	private String Ma_Hoa_Don;
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public String getNgay_Bao_Loi() {
		return Ngay_Bao_Loi;
	}
	public void setNgay_Bao_Loi(String ngayBaoLoi) {
		Ngay_Bao_Loi = ngayBaoLoi;
	}
	public String getThong_Tin_Loi() {
		return Thong_Tin_Loi;
	}
	public void setThong_Tin_Loi(String thongTinLoi) {
		Thong_Tin_Loi = thongTinLoi;
	}
	public String getTen_San_Pham() {
		return Ten_San_Pham;
	}
	public void setTen_San_Pham(String tenSanPham) {
		Ten_San_Pham = tenSanPham;
	}
	public String getMa_Hoa_Don() {
		return Ma_Hoa_Don;
	}
	public void setMa_Hoa_Don(String maHoaDon) {
		Ma_Hoa_Don = maHoaDon;
	}

}
