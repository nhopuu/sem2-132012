package model;

public class SanPhamModel {
	private String ID;
	private String Ten_San_Pham;
	private String Gia_Tien;
	private String Ghi_Chu;
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public String getTen_San_Pham() {
		return Ten_San_Pham;
	}
	public void setTen_San_Pham(String tenSanPham) {
		Ten_San_Pham = tenSanPham;
	}
	public String getGia_Tien() {
		return Gia_Tien;
	}
	public void setGia_Tien(String giaTien) {
		Gia_Tien = giaTien;
	}
	public String getGhi_Chu() {
		return Ghi_Chu;
	}
	public void setGhi_Chu(String ghiChu) {
		Ghi_Chu = ghiChu;
	}

}
